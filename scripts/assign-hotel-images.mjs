#!/usr/bin/env node
/**
 * Assign Cloudinary placeholder images to all 500 hotels
 * Outputs: data/hotel-image-assignments.json + supabase/seed/003_hotel_cloudinary_images.sql
 *
 * Prerequisite: data/cloudinary-hotel-images.json (from upload-hotel-placeholders.mjs)
 * Run: node scripts/assign-hotel-images.mjs
 */
import { readFileSync, writeFileSync, existsSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import {
  generateHotels,
  assignHotelImages
} from "./lib/hotel-seed-lib.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const CLOUDINARY_JSON = join(ROOT, "data/cloudinary-hotel-images.json");
const ASSIGNMENTS_JSON = join(ROOT, "data/hotel-image-assignments.json");
const SQL_OUT = join(ROOT, "supabase/seed/003_hotel_cloudinary_images.sql");

function escSql(s) {
  return String(s ?? "").replace(/'/g, "''");
}

function jsonSql(arr) {
  return `'${escSql(JSON.stringify(arr || []))}'::jsonb`;
}

function buildPools(cloudinaryData) {
  const pools = {};
  for (const [cat, items] of Object.entries(cloudinaryData.categories || {})) {
    pools[cat] = (items || []).map((i) => i.secure_url).filter(Boolean);
  }
  return pools;
}

function main() {
  if (!existsSync(CLOUDINARY_JSON)) {
    console.error("Missing", CLOUDINARY_JSON);
    console.error("Run: node scripts/setup-hotel-seed-assets.mjs && node scripts/upload-hotel-placeholders.mjs");
    process.exit(1);
  }

  const cloudinaryData = JSON.parse(readFileSync(CLOUDINARY_JSON, "utf8"));
  const pools = buildPools(cloudinaryData);
  const emptyCats = Object.entries(pools).filter(([, v]) => !v.length).map(([k]) => k);
  if (emptyCats.length) {
    console.error("Empty Cloudinary pools:", emptyCats.join(", "));
    process.exit(1);
  }

  const hotels = generateHotels();
  const assignments = hotels.map((h, i) => assignHotelImages(h, pools, i));

  writeFileSync(ASSIGNMENTS_JSON, JSON.stringify({
    generated_at: new Date().toISOString(),
    hotel_count: assignments.length,
    image_source: "placeholder",
    assignments
  }, null, 2), "utf8");

  const lines = [
    "-- Hotel Cloudinary placeholder images (MVP seed)",
    "-- Generated by scripts/assign-hotel-images.mjs",
    `-- ${assignments.length} hotels · image_source = placeholder`,
    "-- Run after 002_bulk_inventory.sql",
    ""
  ];

  for (const a of assignments) {
    if (!a.cover_image_url) continue;
    lines.push(`update esm_hotels h set`);
    lines.push(`  cover_image_url = '${escSql(a.cover_image_url)}',`);
    lines.push(`  gallery_image_urls = ${jsonSql(a.gallery_image_urls)},`);
    lines.push(`  gallery_urls = ${jsonSql(a.gallery_urls)},`);
    lines.push(`  room_image_urls = ${jsonSql(a.room_image_urls)},`);
    lines.push(`  image_urls = ${jsonSql(a.image_urls)},`);
    lines.push(`  image_source = 'placeholder'`);
    lines.push(`from esm_cities ci`);
    lines.push(`where h.city_id = ci.id and ci.slug = '${escSql(a.city_slug)}'`);
    lines.push(`  and h.official_name = '${escSql(a.official_name)}';`);
    lines.push("");
  }

  writeFileSync(SQL_OUT, lines.join("\n"), "utf8");
  console.log(`Wrote ${ASSIGNMENTS_JSON}`);
  console.log(`Wrote ${SQL_OUT} (${assignments.length} updates)`);
}

main();
